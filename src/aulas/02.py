usurarios_data_science = {15, 23, 43, 56}
usurarios_machine_learning = {13, 23, 43, 56}

print(usurarios_data_science | usurarios_machine_learning)

print(usurarios_data_science & usurarios_machine_learning)

# Subtração de conjunto
print(usurarios_data_science - usurarios_machine_learning)

# OU exclusivo
print(usurarios_data_science ^ usurarios_machine_learning)